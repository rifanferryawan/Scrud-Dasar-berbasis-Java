  public void tampilData(){
    DefaultTableModel tb=new DefaultTableModel();
    tb.addColumn("NISN");
    tb.addColumn("Nama");
    tb.addColumn("Tempat Lahir");
    tb.addColumn("Tanggal Lahir");
    tb.addColumn("Kelamin");
    tb.addColumn("Nama Ayah");
    tb.addColumn("Nama Ibu");
    tb.addColumn("Alamat");
    tb.addColumn("Telepon");
    tbData.setModel(tb);
    try{
      Connection cn=new Konek().getKonek();
      Statement st=cn.createStatement();
      ResultSet rs=st.executeQuery("Select * from siswa");
      while(rs.next()){
        tb.addRow(new Object[]{
        rs.getString("nisn"),
        rs.getString("nama"),
        rs.getString("tpt_lhr"),
        rs.getString("tgl_lhr"),
        rs.getString("jk"),
        rs.getString("ayah"),
        rs.getString("ibu"),
        rs.getString("alamat"),
        rs.getString("telepon")
      });
      tbData.setModel(tb);
      }
      cn.close();
    }catch(Exception e){}
  }
