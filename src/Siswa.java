      Connection cn=new Konek().getKonek();
      Statement st=cn.createStatement();
      String sql="insert into siswa values ('"+teNisn.getText()+"',"
      +"'"+teNama.getText()+"',"
      +"'"+teTpt.getText()+"',"
      +"'"+teTgl.getText()+"',"
      +"'"+cbJk.getSelectedItem()+"',"
      +"'"+teAyah.getText()+"',"
      +"'"+teIbu.getText()+"',"
      +"'"+teAlm.getText()+"',"
      +"'"+teTel.getText()+"')";
      st.execute(sql);
      st.close();
      cn.close();
      tampilData();
      baru();
