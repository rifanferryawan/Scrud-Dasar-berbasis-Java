  private void btHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHapusActionPerformed
    // TODO add your handling code here:
    String brs=(String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 0);
    try{
      Connection cn=new Konek().getKonek();
      Statement st=cn.createStatement();
      String sql="delete from siswa where nisn='"+brs+"'";
      st.execute(sql);
      st.close();
      cn.close();
      JOptionPane.showMessageDialog(null, "data sukses dihapus");
      tampilData();
      baru();
    }catch(Exception e){
      System.out.println("gagal bos, don't panic");
    }
  }//GEN-LAST:event_btHapusActionPerformed

  private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
    // TODO add your handling code here:
    try{
      if(btEdit.getText()=="Edit"){
        teNisn.setText((String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 0));
        teNisn.setEnabled(false);
        teNama.setText((String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 1));
        teTpt.setText((String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 2));
        teTgl.setText((String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 3));
        teAyah.setText((String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 5));
        teIbu.setText((String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 6));
        teAlm.setText((String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 7));
        teTel.setText((String)tbData.getModel().getValueAt(tbData.getSelectedRow(), 8));
        btEdit.setText("Update");
        btSimpan.setEnabled(false);
        btHapus.setEnabled(false);
      }else{
        try{
          Connection cn=new Konek().getKonek();
          Statement st=cn.createStatement();
          String sql="update siswa set "
                  +"nama='"+teNama.getText()+"',"
                  +"tpt_lhr='"+teTpt.getText()+"',"
                  +"tgl_lhr='"+teTgl.getText()+"',"
                  +"jk='"+cbJk.getSelectedItem()+"',"
                  +"ayah='"+teAyah.getText()+"',"
                  +"ibu='"+teIbu.getText()+"',"
                  +"alamat='"+teAlm.getText()+"',"
                  +"telepon='"+teTel.getText()+"' "
                  +"where nisn='"+teNisn.getText()+"'";
          st.execute(sql);
          st.close();
          cn.close();
          JOptionPane.showMessageDialog(null, "data sukses diupdate");
          tampilData();
          baru();
        }catch(Exception e){
        }
      }
    }catch(Exception e){
      System.out.println("gagal bos, don't panic");
    }
  }//GEN-LAST:event_btEditActionPerformed

